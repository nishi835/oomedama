//------------------------------------------------------------------------------
//　初期化
//------------------------------------------------------------------------------*/
void CMediator::Init(void)
{
	m_Delete = false;
}

//------------------------------------------------------------------------------
//　終了処理
//------------------------------------------------------------------------------
void CMediator::Uninit(void)
{
}

//------------------------------------------------------------------------------
//　更新
//------------------------------------------------------------------------------
void CMediator::Update(void)
{
	m_Rockon->SetPos(m_Enemy->GetPos());

	if (m_Enemy->GetDoUninit())
	{
		m_Rockon->SetDoUninit(true);
		m_Delete = true;
	}
}

//------------------------------------------------------------------------------
//　作成
//------------------------------------------------------------------------------
CMediator* CMediator::Create(CEnemy* Enemy, CRockon* Rockon)
{
	CMediator* pMediator = new CMediator();

	pMediator->SetEnemy(Enemy);
	pMediator->SetRockon(Rockon);
	
	return pMediator;
}

