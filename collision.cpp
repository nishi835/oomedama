/*------------------------------------------------------------------------------
  　初期化
------------------------------------------------------------------------------*/
bool CCollision::Sphere(const D3DXVECTOR3& pos1, const D3DXVECTOR3& pos2, const float& radius1, const float& radius2)
{
	if ((pos2.x - pos1.x) * (pos2.x - pos1.x) + (pos2.y - pos1.y) * (pos2.y - pos1.y) + (pos2.z - pos1.z) * (pos2.z - pos1.z) <= 
		(radius2 + radius1) * (radius2 + radius1))
	{
		return true;
	}

	return false;
}
