#include "main.h"
#include "textureManager.h"
#include "cursorTitle.h"

//------------------------------------------------------------------------------
//　マクロ定義
//------------------------------------------------------------------------------

//------------------------------------------------------------------------------
//  デストラクタ
//------------------------------------------------------------------------------
CCursorTitle::~CCursorTitle()
{
}

//------------------------------------------------------------------------------
// 初期化
//------------------------------------------------------------------------------
void CCursorTitle::Init(void)
{
	// パラメータ初期化
	m_position = 0;

	// scene2Dの初期化
	CScene2D::Init();

	// オブジェクトタイプの設定
	SetObjType(OBJTYPE_NONE);
}

/*------------------------------------------------------------------------------
　終了処理
 ------------------------------------------------------------------------------*/
void CCursorTitle::Uninit(void)
{
	CScene::SetDelete();
}

/*------------------------------------------------------------------------------
　更新
 ------------------------------------------------------------------------------*/
void CCursorTitle::Update(void)
{
	CScene2D::Update();
}

/*------------------------------------------------------------------------------
　ポリゴン描画処理
 ------------------------------------------------------------------------------*/
void CCursorTitle::Draw(void)
{
	CScene2D::Draw();
}

/*------------------------------------------------------------------------------
　作成
 ------------------------------------------------------------------------------*/
CCursorTitle* CCursorTitle::Create(D3DXVECTOR3 pos, D3DXVECTOR3 size, float alpha, char* filePass, CTextureManager::TEXTYPE textype)
{
	CCursorTitle* pImage = new CCursorTitle(CScene::PRIORITY_2D);

	// 初期化
	pImage->SetPos(pos);
	pImage->SetAlpha(alpha);
	pImage->SetSize(size);

	// テクスチャのロード
	pImage->SetTexID(CTextureManager::LoadTexture(filePass, textype));
	pImage->Init();

	return pImage;
}

